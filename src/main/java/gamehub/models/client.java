package gamehub.models;

/**
 *
 * @author abdel
 */
public class client {
     private String id;
    private String name;

    public client(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return name;
    } 
}
